"""
Basic mathematical operations.
"""

def add(a, b):
    """
    Add two numbers.

    :param a: The first number.
    :type a: int or float
    :param b: The second number.
    :type b: int or float
    :return: The sum of a and b.
    :rtype: int or float
    """
    return a + b

def subtract(a, b):
    """
    Subtract the second number from the first.

    :param a: The first number.
    :type a: int or float
    :param b: The second number.
    :type b: int or float
    :return: The difference between a and b.
    :rtype: int or float
    """
    return a - b
